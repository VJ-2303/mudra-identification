def get_mudra_info(mudra_name):
    """
    Get information for a specific mudra.
    Returns a dictionary with description, image, meaning, and usage.
    """
    # Normalize the mudra name (remove "Detected" suffix if present)
    clean_name = mudra_name.replace(" Detected", "").strip()
    
    # Try direct match first
    if clean_name in MUDRA_INFO:
        return MUDRA_INFO[clean_name]
    
    # Try partial match
    for key in MUDRA_INFO.keys():
        if clean_name in key or key in clean_name:
            return MUDRA_INFO[key]
    
    # Return default info if not found
    return {
        "description": "No description available for this mudra.",
        "image": None,
        "meaning": clean_name,
        "usage": "Classical Indian dance gesture."
    }

def get_all_mudras():
    """Get list of all mudra names."""
    return list(MUDRA_INFO.keys())
